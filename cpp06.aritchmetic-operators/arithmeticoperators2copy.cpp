#include <iostream>

int main() {
    int a = 10;
    int b = 3;

    // Addition
    int sum = a + b;
    std::cout << "Sum: " << sum << std::endl;

    // Subtraction
    int difference = a - b;
    std::cout << "Difference: " << difference << std::endl;

    // Multiplication
    int product = a * b;
    std::cout << "Product: " << product << std::endl;

    // Division
    int quotient = a / b;
    std::cout << "Quotient: " << quotient << std::endl;

    // Modulus
    int remainder = a % b;
    std::cout << "Remainder: " << remainder << std::endl;

    return 0;
}